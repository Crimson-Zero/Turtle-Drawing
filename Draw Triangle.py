import turtle

def triangle(edge=100):
    turtle.forward(edge)
    turtle.right(120)
    turtle.forward(edge)
    turtle.right(120)
    turtle.forward(edge)
    turtle.right(120)

    

triangle()
turtle.setheading(60)
triangle()
turtle.setheading(120)
triangle()
turtle.setheading(180)
triangle()
turtle.setheading(240)
triangle()
turtle.setheading(300)
triangle()

